#include<bits/stdc++.h>
 
using namespace std;
 
int main () {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int T = 10;
  cout << T << '\n';
  for (int f = 0; f < T; ++f) {
    int n = (int) 2e5;
    cout << n << '\n';
    for (int i = 0; i < n; ++i) {
      cout << i + 1 << ' ';
    }

    cout << '\n';
  }
}